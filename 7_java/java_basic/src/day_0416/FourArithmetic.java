package day_0416;

public class FourArithmetic {
    public static void main(String[] args) {
        int a = 12; //변수 a 선언과 12할당
        int b = 2;
        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
    }
}
